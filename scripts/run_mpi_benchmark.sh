#!/bin/bash

# Exit immediately if a command exits with a non-zero status
set -e

# --- 1. Automated Build Check ---
if [ ! -d "build" ]; then
    echo "Build directory not found. Creating and compiling..."
    mkdir -p build
    cd build
    cmake ..
    cmake --build .
    cd ..
else
    echo "Ensuring project is compiled..."
    cd build
    cmake --build .
    cd ..
fi

# --- 2. Configuration Variables ---
EXEC=./build/miniClimate
NX=1024
NY=1024
STEPS=500  # Reduced slightly for swift iteration; adjust as needed
OUT_DIR="results"
OUT="$OUT_DIR/mpi_benchmark.csv"

# Create the results directory if it doesn't exist
mkdir -p "$OUT_DIR"

# Clean up any leftover previous tracking files
rm -f benchmark.csv
rm -f "$OUT"

# Pin OpenMP to a single thread for pure MPI scaling measurements
# This ensures we measure distributed process scaling without background threading interference.
export OMP_NUM_THREADS=1

# --- 3. MPI Benchmark Loop ---
# 1024 is evenly divisible by 1, 2, 4, and 8 (Satisfies global_nx % size == 0)
for ranks in 1 2 4 8
do
    echo "=========================================="
    echo "Running Distributed MPI with -np $ranks"
    echo "=========================================="

    # Run using the MPI process execution wrapper
    mpirun -np $ranks \
      $EXEC \
        --nx $NX \
        --ny $NY \
        --steps $STEPS \
        --output "$OUT_DIR/final_field_mpi_${ranks}.dat"
done

# --- 4. Finalizing Results ---
if [ -f "benchmark.csv" ]; then
    mv benchmark.csv "$OUT"
    echo "Success! Pure MPI Benchmark results saved to: $OUT"
else
    echo "Warning: benchmark.csv was not generated by the executable."
fi