#!/bin/bash

# Exit immediately if a command exits with a non-zero status
set -e

# --- 1. Automated Build Check ---
if [ ! -d "build" ]; then
    echo "Build directory not found. Creating and compiling..."
    mkdir -p build
    cd build
    cmake ..
    cmake --build .
    cd ..
else
    # Even if the build folder exists, make sure the executable is compiled
    echo "Ensuring project is compiled..."
    cd build
    cmake --build .
    cd ..
fi

# --- 2. Configuration Variables ---
EXEC=./build/miniClimate
NX=1024
NY=1024
STEPS=2000
OUT_DIR="results"
OUT="$OUT_DIR/openmp_benchmark.csv"

# Create the results directory if it doesn't exist
mkdir -p "$OUT_DIR"

# Clean up any leftover previous files
rm -f benchmark.csv
rm -f "$OUT"

# --- 3. Benchmark Loop ---
for threads in 1 2 4 8
do
    echo "=========================================="
    echo "Running with OMP_NUM_THREADS=$threads"
    echo "=========================================="

    OMP_NUM_THREADS=$threads \
    $EXEC \
      --nx $NX \
      --ny $NY \
      --steps $STEPS \
      --output "$OUT_DIR/final_field_${threads}.dat"
done

# --- 4. Finalizing Results ---
if [ -f "benchmark.csv" ]; then
    mv benchmark.csv "$OUT"
    echo "Success! Benchmark results saved to: $OUT"
else
    echo "Warning: benchmark.csv was not generated by the executable."
fi